package com.entity;


public class Suggestion {

  private int sgid;
  private int rcid;
  private int hid;
  private int aid;
  private double sgprice;


  public int getSgid() {
    return sgid;
  }

  public void setSgid(int sgid) {
    this.sgid = sgid;
  }


  public int getRcid() {
    return rcid;
  }

  public void setRcid(int rcid) {
    this.rcid = rcid;
  }


  public int getHid() {
    return hid;
  }

  public void setHid(int hid) {
    this.hid = hid;
  }


  public int getAid() {
    return aid;
  }

  public void setAid(int aid) {
    this.aid = aid;
  }


  public double getSgprice() {
    return sgprice;
  }

  public void setSgprice(double sgprice) {
    this.sgprice = sgprice;
  }

}
